export function pickFallbackQuote(genres: WorryGenre[]): { text: string; hint: string } {
  const uniq = Array.from(new Set(genres));
  const pool: string[] = [];
  for (const g of uniq) {
    pool.push(...BY_GENRE[g]);
  }
  const text = pool[Math.floor(Math.random() * pool.length)] ?? "波の音を聞きながら、いまの自分を認めてあげよう。";
  const hint =
    uniq.length > 0
      ? `ジャンル「${uniq.map((g) => GENRE_LABELS[g]).join("」「")}」に寄せたメッセージ（オフライン用）`
      : "海のそばのメッセージ（オフライン用）";
  return { text, hint };
}
