function buildExplanation(category) {
  return EXPLANATION_BY_GENRE[category] ?? "この言葉は、いまの心にやさしい視点をくれるための灯りです。";
}

const rows = [];
let t = Date.now();
for (const g of genres) {
  const list = packs[g];
  if (!list || list.length !== 10) {
    throw new Error(`pack size for ${g}: ${list?.length}`);
  }
  list.forEach(([author, content], i) => {
    const id = `q_${g}_${String(i + 1).padStart(2, "0")}`;
    rows.push({
      id,
      category: g,
      author,
      content,
      explanation: buildExplanation(g),
      created_at: t++,
    });
  });
}

mkdirSync(join(root, "data"), { recursive: true });
writeFileSync(join(root, "data", "seed-quotes.json"), JSON.stringify(rows, null, 2), "utf8");
console.log(`Wrote ${rows.length} rows to data/seed-quotes.json`);
